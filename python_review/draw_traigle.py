import turtle


def draw_tra():
	window = turtle.Screen()
	window.bgcolor("purple")
	brad = turtle.Turtle()
	brad.speed(1)
	brad.color("yellow")
	brad.begin_fill()
	brad.right(45)
	brad.forward(200)
	brad.right(-45)
	brad.backward(145)
	brad.right(-90)
	brad.forward(150)
	brad.end_fill()





draw_tra()